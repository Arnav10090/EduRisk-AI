"""
Prediction ORM model.

Represents ML model predictions with SHAP explanations and AI summaries.
"""

from sqlalchemy import Column, String, Integer, Numeric, Boolean, ForeignKey, Text, CheckConstraint, TIMESTAMP
from sqlalchemy.dialects.postgresql import UUID, JSONB
from sqlalchemy.orm import relationship
from sqlalchemy.sql import func
import uuid

from ..db.session import Base


class Prediction(Base):
    """
    Prediction model representing ML model outputs and risk assessments.
    
    Stores complete prediction results including placement probabilities,
    risk scores, salary estimates, SHAP explanations, AI summaries,
    and recommended actions.
    """
    
    __tablename__ = "predictions"
    
    # Primary key
    id = Column(
        UUID(as_uuid=True),
        primary_key=True,
        default=uuid.uuid4,
        server_default=func.gen_random_uuid(),
        comment="Unique prediction identifier"
    )
    
    # Foreign key to student
    student_id = Column(
        UUID(as_uuid=True),
        ForeignKey("students.id", ondelete="CASCADE"),
        nullable=False,
        index=True,
        comment="Reference to student record"
    )
    
    # Model metadata
    model_version = Column(
        String(50),
        nullable=False,
        comment="ML model version used for prediction"
    )
    
    # Placement probabilities
    prob_placed_3m = Column(
        Numeric(5, 4),
        nullable=False,
        comment="Probability of placement within 3 months (0.0000-1.0000)"
    )
    
    prob_placed_6m = Column(
        Numeric(5, 4),
        nullable=False,
        comment="Probability of placement within 6 months (0.0000-1.0000)"
    )
    
    prob_placed_12m = Column(
        Numeric(5, 4),
        nullable=False,
        comment="Probability of placement within 12 months (0.0000-1.0000)"
    )
    
    placement_label = Column(
        String(50),
        nullable=False,
        comment="Placement timeline label (placed_3m, placed_6m, placed_12m, high_risk)"
    )
    
    # Risk assessment
    risk_score = Column(
        Integer,
        nullable=False,
        index=True,
        comment="Composite risk score (0-100, higher = more risk)"
    )
    
    risk_level = Column(
        String(20),
        nullable=False,
        index=True,
        comment="Risk level category (low, medium, high)"
    )
    
    # Salary prediction
    salary_min = Column(
        Numeric(10, 2),
        nullable=True,
        comment="Minimum predicted salary in LPA (Lakhs Per Annum)"
    )
    
    salary_max = Column(
        Numeric(10, 2),
        nullable=True,
        comment="Maximum predicted salary in LPA"
    )
    
    salary_confidence = Column(
        Numeric(4, 2),
        nullable=True,
        comment="Salary prediction confidence percentage"
    )
    
    # EMI affordability
    emi_affordability = Column(
        Numeric(5, 2),
        nullable=True,
        comment="Ratio of loan EMI to predicted monthly salary"
    )
    
    # SHAP explanations (JSONB)
    shap_values = Column(
        JSONB,
        nullable=False,
        comment="Complete SHAP feature attribution values as JSON"
    )
    
    top_risk_drivers = Column(
        JSONB,
        nullable=False,
        comment="Top 5 risk drivers with feature, value, and direction"
    )
    
    # AI-generated summary
    ai_summary = Column(
        Text,
        nullable=True,
        comment="Natural language risk summary generated by LLM"
    )
    
    # Action recommendations (JSONB)
    next_best_actions = Column(
        JSONB,
        nullable=False,
        comment="Recommended interventions as JSON array"
    )
    
    # Alert flag
    alert_triggered = Column(
        Boolean,
        nullable=False,
        default=False,
        server_default="false",
        comment="Whether high-risk alert was triggered"
    )
    
    # Timestamp
    created_at = Column(
        TIMESTAMP(timezone=True),
        nullable=False,
        server_default=func.now(),
        index=True,
        comment="Prediction creation timestamp"
    )
    
    # Constraints
    __table_args__ = (
        CheckConstraint(
            "prob_placed_3m >= 0 AND prob_placed_3m <= 1",
            name="check_prob_3m_range"
        ),
        CheckConstraint(
            "prob_placed_6m >= 0 AND prob_placed_6m <= 1",
            name="check_prob_6m_range"
        ),
        CheckConstraint(
            "prob_placed_12m >= 0 AND prob_placed_12m <= 1",
            name="check_prob_12m_range"
        ),
        CheckConstraint(
            "risk_score >= 0 AND risk_score <= 100",
            name="check_risk_score_range"
        ),
        CheckConstraint(
            "risk_level IN ('low', 'medium', 'high')",
            name="check_risk_level_enum"
        ),
        CheckConstraint(
            "placement_label IN ('placed_3m', 'placed_6m', 'placed_12m', 'high_risk')",
            name="check_placement_label_enum"
        ),
    )
    
    # Relationships
    student = relationship(
        "Student",
        back_populates="predictions"
    )
    
    audit_logs = relationship(
        "AuditLog",
        back_populates="prediction",
        lazy="selectin"
    )
    
    def __repr__(self) -> str:
        return (
            f"<Prediction(id={self.id}, student_id={self.student_id}, "
            f"risk_level='{self.risk_level}', risk_score={self.risk_score})>"
        )
